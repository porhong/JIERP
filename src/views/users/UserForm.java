    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        try {
            if (CheckData.checktext(txtUserName, txtPassword) == 0) {
                return;
            }
            user = new User();
            
            user.accountTypeId = user.getAccountId(cboAccountTypeName);
            user.userName = txtUserName.getText();
            user.password = txtPassword.getText();
            if (rTrue.isSelected()) {
                user.active = rTrue.getText();
            } else {
                user.active = rFalse.getText();
            }
            user.save();
            
        } catch (Exception ex) {
            JOptionPane.showConfirmDialog(null, "error:" + ex.getMessage());
        }

    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        //if(tblUser.getRowCount()==0){
        //    return;
        //}
        //if(tblUser.getSelectionModel().isSelectionEmpty()){
        //   return;
        //}
        int selectedRow = tblUser.getSelectedRow();
        user = new User();
        user.mode=(DefaultTableModel)tblUser.getModel();
        int click = JOptionPane.showConfirmDialog(null, "Do you want to delete record?", "Delete", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (click == JOptionPane.YES_OPTION) {
            user.id = Integer.valueOf(tblUser.getValueAt(selectedRow, 0).toString());
            user.delete();
            user.mode.removeRow(selectedRow);
            
        }


    }//GEN-LAST:event_btnDeleteActionPerformed

