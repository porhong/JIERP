    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

        user=new User();
        try {
            int selectedRow=tblUser.getSelectedRow();
            user.id=Integer.valueOf(tblUser.getValueAt(selectedRow, 0).toString());
            user.accountTypeId=user.getAccountId(cboAccountTypeName);
            user.userName=txtUserName.getText();
            user.password=txtPassword.getText();
            if(rTrue.isSelected()){
                user.active=rTrue.getText();
            }else{
                user.active=rFalse.getText();
            }
            user.update();
            
        } catch (Exception ex) {
            JOptionPane.showConfirmDialog(null, "Error:"+ex.getMessage());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void txtSearchKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyPressed
        if(evt.getKeyChar()==KeyEvent.VK_ENTER){
            user=new User();
            user.userName=txtSearch.getText().trim();
            user.search(tblUser);
        }
        
    }//GEN-LAST:event_txtSearchKeyPressed


